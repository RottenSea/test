# 定义葫芦娃颜色列表
# 索引 0-6 分别对应：大娃、二娃、三娃、四娃、五娃、六娃、七娃
hulu_colors = ["红色", "橙色", "黄色", "绿色", "青色", "蓝色", "紫色"]

try:
    # 获取用户输入并转换为整数
    num = int(input("请输入葫芦娃的编号 (1-7): "))

    # 检查输入范围是否合法
    if 1 <= num <= 7:
        # 利用索引知识：列表下标 = 编号 - 1
        print(f"{num}号葫芦娃的代表颜色是：{hulu_colors[num - 1]}")
    else:
        print("输入错误，请输入 1 到 7 之间的数字。")
except ValueError:
    print("请输入有效的数字！")
